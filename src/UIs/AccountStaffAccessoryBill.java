    private void jB_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_UpdateActionPerformed
        try {
            String sqlString = "UPDATE BILLACCOUNT SET BILID = ?, BILDATE = ?, BILAUTHORIZED = ?, BILEXPENSE = ?, BILPAYMODE = ?, "
                    + "BILSTAFFID=?, BILSTAFFNAME=?, BILSTAFFPHOTO=?, BILSTAFFADDR=?, BILSTAFFCONTACT=?, BILSTAFFSALARY=?, BILBILLOF=?, "
                    + "BILBILLTO=?, BILUNTAMOUNT=?, BILQUANTITY=?, BILITEMS=?, BILTOTALAMOUNT=?, BILPAYDATE=?, BILPAYDUE=?, BILACCNUMBER=?,"
                    + "BILACCNAME=?, BILBNKNAME=?, BILBRNCNAME=?, BILSWIFTCODE=?, BILCITY=?, BILCOUNTRY=? WHERE BILID = ?";
            aM = new AccountModel();
            aM.Id = jTF_BillId.getText().toUpperCase();
            aM.Date = ((JTextField) jDC_BillDate.getDateEditor().getUiComponent()).getText();
            aM.Name = jCB_AuthorizedBy.getSelectedItem().toString().toUpperCase();
            aM.ExpenseType = jCB_ExpenseType.getSelectedItem().toString().toUpperCase();
            aM.PaymentMode = jCB_PaymentMode.getSelectedItem().toString().toUpperCase();
            if (jCB_ExpenseType.getSelectedItem().toString().equals("Peon Salary") || jCB_ExpenseType.getSelectedItem().toString().equals("Cleaners Salary")
                    || jCB_ExpenseType.getSelectedItem().toString().equals("Security Salary") || jCB_ExpenseType.getSelectedItem().toString().equals("Driver Salary")) {
                aM.StaffId = jTF_StaffId.getText().toUpperCase();
                aM.StaffName = jTF_StaffName.getText().toUpperCase();
                aM.PhotographInByte = imageModel.ConvertImageToByteArray(jL_StaffPhoto.getIcon());
                aM.StaffAddress = jTF_StaffAdress.getText().toUpperCase();
                aM.StaffContactNo = jTF_StaffContactNo.getText().toUpperCase();
                aM.SalaryAmount = jTF_SalaryAmount.getText().toUpperCase();
            } else {
                aM.BillOf = jTF_BillOf.getText().toUpperCase();
                aM.BillTo = jTF_BillTo.getText().toUpperCase();
                aM.UnitAmount = jTF_UnitAmount.getText().toUpperCase();
                aM.Quantity = jTF_Quantity.getText().toUpperCase();
                aM.ItemLists = jTA_ItemLists.getText().toUpperCase();
                aM.TotalAmount = jTF_TotalAmount.getText().toUpperCase();
            }
            if (jDC_CashPaymentDate.isEnabled()) {
                aM.PaymentDate = ((JTextField) jDC_CashPaymentDate.getDateEditor().getUiComponent()).getText();
                if (jCB_CashPaymentDue.isSelected()) {
                    aM.DueAmount = jTF_CashPaymentDueAmount.getText().toUpperCase();
                } else {
                    aM.DueAmount = "Not Selected";
                }
            } else if (jDC_ChequePaymentDate.isEnabled()) {
                aM.PaymentDate = ((JTextField) jDC_ChequePaymentDate.getDateEditor().getUiComponent()).getText();
                aM.AccountNumber = jTF_ChequePaymentAccNum.getText().toUpperCase();
                aM.AccountName = jTF_ChequePaymentAccName.getText().toUpperCase();
                aM.BankName = jTF_ChequePaymentBankName.getText().toUpperCase();
                aM.BranchName = jTF_ChequePaymentBranchName.getText().toUpperCase();
            } else {
                aM.PaymentDate = ((JTextField) jDC_OnlinePaymentDate.getDateEditor().getUiComponent()).getText();
                aM.AccountNumber = jTF_OnlinePaymentAccNum.getText().toUpperCase();
                aM.AccountName = jTF_OnlinePaymentAccName.getText().toUpperCase();
                aM.BankName = jTF_OnlinePaymentBankName.getText().toUpperCase();
                aM.BranchName = jTF_OnlinePaymentBranchName.getText().toUpperCase();
                aM.SwiftCode = jTF_OnlinePaymentSwiftCode.getText().toUpperCase();
                aM.City = jTF_OnlinePaymentCity.getText().toUpperCase();
                aM.Country = jTF_OnlinePaymentCountry.getText().toUpperCase();
            }
            aM.SearchBoxId = jTF_SearchBox.getText().toUpperCase();
            
            String feedBack = aM.StaffAccessoryBillValidityCheck();
            if ("Checked!!!".equals(feedBack)) {
                feedBack = aM.UpdateStaffAccessoryBill(sqlString);
            }
            JOptionPane.showMessageDialog(this, feedBack);
            RefreshBills();
        } catch (SQLException ex) {
            Logger.getLogger(AccountStaffAccessoryBill.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jB_UpdateActionPerformed

