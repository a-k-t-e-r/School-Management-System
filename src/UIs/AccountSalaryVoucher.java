    private void jTF_ConveyanceAllowanceKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTF_ConveyanceAllowanceKeyReleased
        double basicDA = Double.parseDouble(jT_BasicDA.getText());
        double hRA = Double.parseDouble(jT_HRA.getText());
        double conveyanceAllowance = Double.parseDouble(jTF_ConveyanceAllowance.getText());
        double wholePercentAmount = (basicDA * hRA / 100) + (basicDA * conveyanceAllowance / 100);
        double totalEarnings = basicDA + wholePercentAmount;
        jTF_TotalEarnings.setText(totalEarnings + "");
    }//GEN-LAST:event_jTF_ConveyanceAllowanceKeyReleased

    private void jTF_TSDITKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTF_TSDITKeyReleased
        jTF_TotalDeduction.setText("");
        jTF_NetSalary.setText("");
        
        double totalEarnings = Double.parseDouble(jTF_TotalEarnings.getText());
        double providentFund = Double.parseDouble(jTF_ProvidentFund.getText());
        double eSI = Double.parseDouble(jTF_ESI.getText());
        double loan = Double.parseDouble(jTF_Loan.getText());
        double professionTax = Double.parseDouble(jTF_ProfessionTax.getText());
        double tSDIT = Double.parseDouble(jTF_TSDIT.getText());
        double totalDeduction = (providentFund - eSI - loan - (totalEarnings * professionTax / 100) - tSDIT);
        jTF_TotalDeduction.setText(totalDeduction + "");
        double netSalary = totalEarnings + totalDeduction;
        jTF_NetSalary.setText(netSalary + "");
    }//GEN-LAST:event_jTF_TSDITKeyReleased

