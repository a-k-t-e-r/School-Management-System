    private void GetBothEmpTea() {
        String sqlString = "", feedBack = "";
        try {
            aM = new AccountModel();
            if (jTF_TeaEmpId.getText().toUpperCase().substring(0, 3).equals("EMP")) {
                sqlString = "SELECT EMPID, EMPNAME, EMPPOSITION FROM EMPLOYEEDATASET WHERE EMPID = ?";
            } else if (jTF_TeaEmpId.getText().toUpperCase().substring(0, 3).equals("TEA")) {
                sqlString = "SELECT TEAID, TEANAME, TEADESIGNATION FROM TEACHERDATASET WHERE TEAID = ?";
            }
            aM.SearchBoxId = jTF_TeaEmpId.getText().toUpperCase();
            feedBack = aM.SearchTeacherOrEmployee(sqlString);
            if ("Information Retrive".equals(feedBack)) {
                jTF_TeaEmpId.setText(aM.Id);
                jL_TeaEmpName.setText(aM.Name);
                jL_TeaEmpDesignationPosition.setText(aM.Position_Designation);
            }
        } catch (SQLException ex) {
            Logger.getLogger(AccountSalaryVoucher.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

