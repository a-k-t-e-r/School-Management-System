    private void seekForSearcher(String fromWho) {
        try {
            String sqlCondition, sqlString = "", searchId = "", searchByAnother = "";
            
            if ("iStudent".equals(fromWho)) {
                stuProModel = new StudentsModel();
                stuProModel.searchTableModel = (DefaultTableModel) jT_StudentProfile.getModel();
                stuProModel.searchTableModel.setRowCount(0);
                if (jL_StudentAnd.getForeground() == Color.RED) {
                    sqlCondition = "AND";
                } else {
                    sqlCondition = "OR";
                }
                sqlString = "SELECT * FROM STUDENTDATASET WHERE STUID = ? " + sqlCondition + " STUCLASS = ?";
                searchId = jTF_StudentId.getText().toUpperCase();
                searchByAnother = jCB_Class.getSelectedItem().toString();
                searchDatasets = stuProModel.SearchForStudents(sqlString, searchId, searchByAnother);
            } else if ("iTeacher".equals(fromWho)) {
                teaProModel = new TeachersModel();
                teaProModel.searchTableModel = (DefaultTableModel) jT_TeacherProfile.getModel();
                teaProModel.searchTableModel.setRowCount(0);
                if (jL_TeacherAnd.getForeground() == Color.RED) {
                    sqlCondition = "AND";
                } else {
                    sqlCondition = "OR";
                }
                sqlString = "SELECT * FROM TEACHERDATASET WHERE TEAID = ? " + sqlCondition + " TEADESIGNATION = ?";
                searchId = jTF_TeacherId.getText().toUpperCase();
                searchByAnother = jCB_Designation.getSelectedItem().toString();
                searchDatasets = teaProModel.SearchForTeachers(sqlString, searchId, searchByAnother);
            } else if ("iEmployee".equals(fromWho)) {
                empProModel = new EmployeeModel();
                empProModel.searchTableModel = (DefaultTableModel) jT_EmployeeProfile.getModel();
                empProModel.searchTableModel.setRowCount(0);
                if (jL_EmployeeAnd.getForeground() == Color.RED) {
                    sqlCondition = "AND";
                } else {
                    sqlCondition = "OR";
                }
                sqlString = "SELECT * FROM EMPLOYEEDATASET WHERE EMPID = ? " + sqlCondition + " EMPPOSITION = ?";
                searchId = jTF_EmployeeId.getText().toUpperCase();
                searchByAnother = jCB_Position.getSelectedItem().toString();
                searchDatasets = empProModel.SearchForEmployees(sqlString, searchId, searchByAnother);
            } else {
                System.out.println("Check The seekForSearcher() Method.");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(ProfilesSearch.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

